//Aplicação para mostrar um exemplo de condição usando o if
//Declarando a classe Condicao
public class Condicao
{
	//Declarando a classe principal
	 public static void main(String[] args) 
	 {
		//Declarando uma variavel no qual vai receber um valor
		int media = 68;
		//Empregando o if para fazer a condiçao
		if (media >60)
		{
			System.out.println("Voce passou com " + media);
		}
	}
}